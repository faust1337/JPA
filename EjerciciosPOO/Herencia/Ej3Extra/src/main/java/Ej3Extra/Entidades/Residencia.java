package Ej3Extra.Entidades;
public class Residencia extends Extrahotelero{
    private int cantHabitaciones;
    private boolean descuentoGremios, campoDeportivo;

    public Residencia(int cantHabitaciones, boolean descuentoGremios, boolean campoDeportivo, String nombre, boolean privado, int superficie) {
        super(nombre, privado, superficie);
        this.cantHabitaciones = cantHabitaciones;
        this.descuentoGremios = descuentoGremios;
        this.campoDeportivo = campoDeportivo;
    }
    

    public int getCantHabitaciones() {
        return cantHabitaciones;
    }

    public void setCantHabitaciones(int cantHabitaciones) {
        this.cantHabitaciones = cantHabitaciones;
    }

    public boolean isDescuentoGremios() {
        return descuentoGremios;
    }

    public void setDescuentoGremios(boolean descuentoGremios) {
        this.descuentoGremios = descuentoGremios;
    }

    public boolean isCampoDeportivo() {
        return campoDeportivo;
    }

    public void setCampoDeportivo(boolean campoDeportivo) {
        this.campoDeportivo = campoDeportivo;
    }
    
}
